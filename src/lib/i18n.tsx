// Languages without full translations fall back to English on every key.
// Adding them here lets the language picker switch <html lang> and remember
// the user's choice while we ship full localizations later.
const fallbackLangs: Language[] = [];
const translationsAll: Record<Language, typeof translations.en> = {
  ...(translations as any),
  ...Object.fromEntries(fallbackLangs.map((l) => [l, translations.en])),
} as Record<Language, typeof translations.en>;

type TranslationKey = keyof typeof translations.en;

interface I18nContextType {
  lang: Language;
  setLang: (lang: Language) => void;
  t: (key: TranslationKey) => string;
}

const I18nContext = createContext<I18nContextType | null>(null);

export const I18nProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [lang, setLangState] = useState<Language>(() => {
    const saved = localStorage.getItem("wardrobe-lang");
    if (saved) return saved as Language;
    if (typeof navigator !== "undefined") {
      const browser = (navigator.language || "en").toLowerCase().slice(0, 2);
      const supported: Language[] = ["de", "en", "es", "fr", "it", "ja", "ko", "zh"];
      if (supported.includes(browser as Language)) return browser as Language;
    }
    return "en";
  });

  // Keep <html lang> in sync so :lang(ko) CSS + Korean font rules apply globally.
  React.useEffect(() => {
    if (typeof document !== "undefined") {
      document.documentElement.lang = lang;
    }
  }, [lang]);

  const setLang = useCallback((l: Language) => {
    setLangState(l);
    localStorage.setItem("wardrobe-lang", l);
  }, []);

  const t = useCallback(
    (key: TranslationKey) => translationsAll[lang]?.[key] || translations.en[key] || key,
    [lang]
  );

  return <I18nContext.Provider value={{ lang, setLang, t }}>{children}</I18nContext.Provider>;
};

export const useI18n = () => {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used within I18nProvider");
  return ctx;
};
